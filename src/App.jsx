import { useState } from 'react';
import 'bulma/css/bulma.css';
import './App.scss';

export const goods = [
  'Dumplings',
  'Carrot',
  'Eggs',
  'Ice cream',
  'Apple',
  'Bread',
  'Fish',
  'Honey',
  'Jam',
  'Garlic',
];

export const App = () => {
  const INITIAL_VALUE = 'Jam';
  const EMPTY_MESSAGE = 'No goods selected';

  const [selectedGood, setMessage] = useState(INITIAL_VALUE);
  const [isButtonVisible, setIsButtonVisible] = useState(true);

  const handleClearSelection = () => {
    setMessage(EMPTY_MESSAGE);
    setIsButtonVisible(false);
  };

  const handleUpdateSelection = goodValue => {
    setMessage(goodValue);
    setIsButtonVisible(true);
  };

  return (
    <main className="section container">
      {isButtonVisible ? (
        <h1 className="title is-flex is-align-items-center">
          {selectedGood} is selected
          <button
            data-cy="ClearButton"
            type="button"
            className="delete ml-3"
            onClick={handleClearSelection}
          />
        </h1>
      ) : (
        <h1 className="title">{EMPTY_MESSAGE}</h1>
      )}

      <table className="table">
        <tbody>
          {goods.map(good => (
            <tr
              key={good}
              data-cy="Good"
              className={
                selectedGood === good ? 'has-background-success-light' : ''
              }
            >
              <td>
                {selectedGood === good ? (
                  <button
                    data-cy="RemoveButton"
                    type="button"
                    className="button is-info"
                    onClick={handleClearSelection}
                  >
                    -
                  </button>
                ) : (
                  <button
                    data-cy="AddButton"
                    type="button"
                    className="button"
                    onClick={() => handleUpdateSelection(good)}
                  >
                    +
                  </button>
                )}
              </td>

              <td data-cy="GoodTitle" className="is-vcentered">
                {good}
              </td>
            </tr>
          ))}
        </tbody>
      </table>
    </main>
  );
};
